
public class ForEx1 {
	/*
	 * 	for(초기화;조건식;증감식){
	 * 		반복할 코드
	 *  }
	 */
	public static void main(String[] args) {
		int i;
		
		for(i=0;i<5;i++) {
			System.out.println("Hello World");
		}
		System.out.println("---------------");
		for(int j=0;j<5;j++) 
			System.out.println("Hello World");
		//System.out.println(j);
		//j가 반복문에서 선언을 했기 떄문에 반복문이 끝나면 사라짐
		

	}

}
