
public class FloatDouble {
	/*
	 * float double 소수점을 저장할 수 있는 변수 타입
	 */
	public static void main(String[] args) {
		float f1 = 3.1415f;
		double d1 = 3.1415;
		
		//반지름 3인 원넓이 출력
		System.out.println(3*3*f1);//정수형 데이터인 3*3이 f1 데이터 타입인 float로 형변환
		System.out.println(3*3*d1);//정수형 데이터인 3*3이 d1 데이터 타입인 double로 형변환
		

	}

}
